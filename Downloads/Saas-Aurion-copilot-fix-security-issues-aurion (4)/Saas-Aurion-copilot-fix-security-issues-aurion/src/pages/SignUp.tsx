interface PupilProps {

  size?: number;

  maxDistance?: number;

  pupilColor?: string;

  forceLookX?: number;

  forceLookY?: number;

}



const Pupil = ({

  size = 12,

  maxDistance = 5,

  pupilColor = "black",

  forceLookX,

  forceLookY

}: PupilProps) => {

  const [mouseX, setMouseX] = useState<number>(0);

  const [mouseY, setMouseY] = useState<number>(0);

  const pupilRef = useRef<HTMLDivElement>(null);



  useEffect(() => {

    const handleMouseMove = (e: MouseEvent) => {

      setMouseX(e.clientX);

      setMouseY(e.clientY);

    };



    window.addEventListener("mousemove", handleMouseMove);



    return () => {

      window.removeEventListener("mousemove", handleMouseMove);

    };

  }, []);

  // Calculate pupil position based on mouse coordinates
  const pupilPosition = useMemo(() => {
    // If forced look direction is provided, use that instead of mouse tracking
    if (forceLookX !== undefined && forceLookY !== undefined) {
      return { x: forceLookX, y: forceLookY };
    }
    
    // Calculate based on mouse position (simplified without ref access during render)
    // The ref access is handled via the useEffect that updates mouseX/mouseY
    const deltaX = mouseX - window.innerWidth / 2;
    const deltaY = mouseY - window.innerHeight / 2;
    const distance = Math.min(Math.sqrt(deltaX ** 2 + deltaY ** 2), maxDistance * 50);
    
    if (distance === 0) return { x: 0, y: 0 };
    
    const angle = Math.atan2(deltaY, deltaX);
    const normalizedDistance = Math.min(distance / 100, maxDistance);
    const x = Math.cos(angle) * normalizedDistance;
    const y = Math.sin(angle) * normalizedDistance;
    
    return { x, y };
  }, [mouseX, mouseY, forceLookX, forceLookY, maxDistance]);

  return (

    <div

      ref={pupilRef}

      className="rounded-full"

      style={{

        width: `${size}px`,

        height: `${size}px`,

        backgroundColor: pupilColor,

        transform: `translate(${pupilPosition.x}px, ${pupilPosition.y}px)`,

        transition: 'transform 0.1s ease-out',

      }}

    />

  );

};









